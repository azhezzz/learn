function Width(){
	document.getElementsByClassName("box")[0].style.width=200+"px";
}
function Height(){
	document.getElementsByClassName("box")[0].style.height="200px";
}
function Color(){
	document.getElementsByClassName("box")[0].style.backgroundColor="red";
}
function Hide(){
	document.getElementsByClassName("box")[0].style.display="none";
}
function Reset(){
	document.getElementsByClassName("box")[0].style.display="";
	document.getElementsByClassName("box")[0].style.backgroundColor="pink";
	document.getElementsByClassName("box")[0].style.width="100px";
	document.getElementsByClassName("box")[0].style.height="100px";
}